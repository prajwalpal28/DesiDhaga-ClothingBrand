const mongoose = require('mongoose');

const UserSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true,
    },
    email: {
        type: String,
        required: true,
    },
    password: {
        type: String,
        required: true,
    }
});

const UserRegisterModel = mongoose.model("register", UserSchema, "register");
module.exports = UserRegisterModel;
